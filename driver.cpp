void PlayerBoard(Hand &theHand) {
    std::cout << "[----------------------------]" << std::endl;
    std::cout << "| HEALTH: " << theHand._health << std::endl;
    for (int i = 0; i < theHand.GetSize(); i++) {
        std::cout << "| (" << (i + 1) << ")\t\t" << theHand.set.at(i)._face << "\t" << theHand.set.at(i)._value << std::endl;
    }
    std::cout << "[----------------------------]" << std::endl;
}

